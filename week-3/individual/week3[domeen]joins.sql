ROLL A Müük+Kliendid:
  
Analüüs näitab, et ainult 29,91% klientidest kulutab üle keskmise, mis viitab sellele, et UrbanStyle’i käive sõltub suuresti väiksemast grupist väärtuslikest klientidest.
Seetõttu peaks ettevõte keskenduma lojaalsete ja suure kulutusega klientide hoidmisele ning pakkuma neile personaalseid kampaaniaid ja lojaalsusprogramme. 
Samal ajal võiks ettevõte proovida motiveerida väiksema kulutusega kliente rohkem ostma, et kasvatada üldist müüki.
Analüüsi põhjal on UrbanStylei tugevaim müügipiirkond Tallinn, mis toob ettevõttele kõige suurema käibe ja kliendiarvu.
Tartu on samuti oluline turg ning seal võiks kaaluda täiendavaid turunduskampaaniaid, et kasvatada müüki veelgi.
TOP-klientide analüüs näitab, et osa kliente teeb väga sagedasi oste või kulutab märkimisväärselt palju raha, mis viitab lojaalsete püsiklientide suurele väärtusele ettevõtte jaoks.
Samuti loyalty tier analüüs, et kõrgema taseme kliendid kulutavad keskmiselt rohkem, seega tasub lojaalsusprogrammidesse edasi investeerida.

